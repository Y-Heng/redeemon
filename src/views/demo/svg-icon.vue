<template>
  <div class="icons-container">
    <div class="icons-wrapper">
      <div v-for="item of icons_map" :key="item" @click="handleClipboard(generateIconCode(item),$event)">
        <el-tooltip placement="top">
          <div slot="content">
            {{generateIconCode(item)}}
          </div>
          <div class="icon-item">
            <svg-icon class-name="disabled" :icon-class="item" />
            <span>{{item}}</span>
          </div>
        </el-tooltip>
      </div>
    </div>
  </div>
</template>

<script lang="ts">
import icons from '@/icons/view'
import clipboard from '@/utils/clipboard'
import { Component, Vue } from 'vue-property-decorator'

@Component({})
export default class SvgIconDemon extends Vue {
  icons_map: string[] = []
  generateIconCode(symbol: any): string {
    return `<svg-icon icon-class="${symbol}" />`
  }
  handleClipboard(text: any, event: any) {
    clipboard(text, event)
  }
  mounted() {
    const iconsMap = icons.state.iconsMap.map((i: any) => {
      return i.default.id.split('-')[1]
    })
    this.icons_map = iconsMap
  }
}
</script>

